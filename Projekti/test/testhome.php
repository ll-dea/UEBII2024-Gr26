        $(document).ready(function() {
            var carousel1 = $("#carouselExampleControls1");
            var carousel2 = $("#carouselExampleControls2");
            var carousel3 = $("#carouselExampleControls3");

            var cardWidth = $(".carousel-item").width();
            var scrollPosition1 = 0;
            var scrollPosition2 = 0;
            var scrollPosition3 = 0;

            function scrollCarousel(carousel, scrollPosition) {
                carousel.find(".carousel-inner").animate({
                    scrollLeft: scrollPosition
                }, 600);
            }

            carousel1.find(".carousel-control-next").on("click", function() {
                if (scrollPosition1 < carousel1.find(".carousel-inner")[0].scrollWidth - cardWidth * 4) {
                    scrollPosition1 += cardWidth;
                    scrollCarousel(carousel1, scrollPosition1);
                }
            });

            carousel1.find(".carousel-control-prev").on("click", function() {
                if (scrollPosition1 > 0) {
                    scrollPosition1 -= cardWidth;
                    scrollCarousel(carousel1, scrollPosition1);
                }
            });

            carousel2.find(".carousel-control-next").on("click", function() {
                if (scrollPosition2 < carousel2.find(".carousel-inner")[0].scrollWidth - cardWidth * 4) {
                    scrollPosition2 += cardWidth;
                    scrollCarousel(carousel2, scrollPosition2);
                }
            });

            carousel2.find(".carousel-control-prev").on("click", function() {
                if (scrollPosition2 > 0) {
                    scrollPosition2 -= cardWidth;
                    scrollCarousel(carousel2, scrollPosition2);
                }
            });

            carousel3.find(".carousel-control-next").on("click", function() {
                if (scrollPosition3 < carousel3.find(".carousel-inner")[0].scrollWidth - cardWidth * 4) {
